/*Programa que muestra los elementos de un arreglo unidimensional de cinco elementos del tipo char*/

#include<stdio.h>

int main(){
    char miArreglo[4];

    miArreglo[0]='H';
    miArreglo[1]='O';
    miArreglo[2]='L';
    miArreglo[3]='A';

    printf("\t\n %c",miArreglo[0]);
    printf("\t\n %c",miArreglo[1]);
    printf("\t\n %c",miArreglo[2]);
    printf("\t\n %c",miArreglo[3]);


return 0;
}
